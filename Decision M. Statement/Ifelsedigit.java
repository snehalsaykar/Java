//write java program to check wether given character is a digit.
class Ifelsedigit {
    public static void main(String[] args) {
        char ch = '7'; // A , a
        if (ch >= '0' && ch <= '9') {
            System.out.println("character is digit");
        } else {
            System.out.println("character is not digit");
        }
    }

}
